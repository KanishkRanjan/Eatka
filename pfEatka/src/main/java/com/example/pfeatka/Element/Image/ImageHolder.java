package com.example.pfeatka.Element.Image;

import javafx.scene.image.Image;

public class ImageHolder {
    public Image image = null;
    public double height = 0;
    public double width =0 ;

    public ImageHolder(Image image, double height, double width){
        this.image = image;
        this.height = height;
        this.width = width;
    }
}
